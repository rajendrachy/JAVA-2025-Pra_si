package LAC1_College_wallah.CODE;
//------------substring(i, j) and substring(i)----------------- 
// Substring is a continuous part of the string
public class substring {
  public static void main(String[] args) {
    String str = "Rajendra Chaudhary";

System.out.println(str.substring(0,0)); // empty string
    

    System.out.println(str.substring(0, 6)); // Rajend

    System.out.println(str.substring(7, 15)); // a Chaudh


    System.out.println(str.substring(7)); // a Chaudhary


    System.out.println(str.substring(0)); // Rajendra Chaudhary



    System.out.println(str.substring(16)); // ry

  }
}









  



